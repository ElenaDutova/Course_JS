function fib(number) {
    if (typeof(number) != 'number' || number <= 0 || !Number.isInteger(number)) {
        return "";
    } else {
        let result = [];
        for (let i = 0; i < number; i++) {
            result[i] = i;
            if (i >= 2) {
                result[i] = result[i - 1] + result[i - 2];
            }
        }
        return result.join(' ');
    }
}

